package Grafos.aplicacao;

/**
 *
 * @author Guilherme
 */
public class Campeao {
    private String nome;
    private String funcao;

    public Campeao() {
    }

    public Campeao(String nome, String funcao) {
        this.nome = nome;
        this.funcao = funcao;
    }

    public String getNome() {
        return nome;
    }

    public String getFuncao() {
        return funcao;
    }

    public void setFuncao(String funcao) {
        this.funcao = funcao;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
    
    
    
    
    
    
    
    
    
    
}
